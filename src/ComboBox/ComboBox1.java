    public int obtenerMayor() {
        int numeroMayor = Integer.parseInt(lstValor.get(0));
        for (String valor : lstValor) {
            if (Integer.parseInt(valor) > numeroMayor) {
                numeroMayor = Integer.parseInt(valor);
            }
        }
        return numeroMayor;
    }

    public int obtenerMenor() {
        int numeroMayor = Integer.parseInt(lstValor.get(0));
        for (String valor : lstValor) {
            if (Integer.parseInt(valor) < numeroMayor) {
                numeroMayor = Integer.parseInt(valor);
            }
        }
        return numeroMayor;
    }

    public void agregarItems() {
        cmbColores.removeAllItems();
        for (String datos : lstValor) {
            cmbColores.addItem(datos);
        }

    }

