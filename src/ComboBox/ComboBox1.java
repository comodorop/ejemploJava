    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        for (int x = 0; x < lstValor.size(); x++) {
            String valorCmb = (String) cmbColores.getSelectedItem();
            if (lstValor.get(x).equals(valorCmb)) {
                lstValor.remove(x);
                break;
            }
        }
        cmbColores.removeItemAt(cmbColores.getSelectedIndex());
    }//GEN-LAST:event_jButton3ActionPerformed

